import mongoose from 'mongoose';

const salarySchema = new mongoose.Schema({
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true,
        unique: true
    },
    basicSalary: {
        type: Number,
        required: true
    },
    hra: {
        type: Number,
        default: 0
    },
    da: {
        type: Number,
        default: 0
    },
    deductions: {
        type: Number,
        default: 0
    },
    netSalary: {
        type: Number,
        required: true
    },
    effectiveFrom: {
        type: Date,
        default: Date.now
    }
}, {
    timestamps: true
});

const Salary = mongoose.model('Salary', salarySchema);

export default Salary;
